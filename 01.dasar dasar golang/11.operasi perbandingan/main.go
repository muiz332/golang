/*


- operasi perbandingan

adalah sebuah operasi yang membandingkan dua atau lebih operan yang menghasilkan
nilai true atau false dimana ini disebut dengan kondisi

macam macam operasi perbandingan

>  lebih besar
<  lebih kecil
=> lebih besar sama dengan
<= lebih kecil sama dengan
!= tidak sama dengan
== sama dengan

langsung saja kita coba

*/

package main

import "fmt"

func main() {

	var angka1 int8 = 10
	fmt.Println(angka1 < 20)
	fmt.Println(angka1 <= 10)
	fmt.Println(angka1 != 10)
	fmt.Println(angka1 == 10)
	fmt.Println(angka1 > 6)
	fmt.Println(angka1 >= 10)

}
